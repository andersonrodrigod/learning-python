# Built-in Math Functions


x = min(5, 10, 25)
y = max(5, 10, 25)

print(x)
print(y)

# Built-in Math Functions returm (value positive)

b = abs(-7.25)

print(b)


# Built-in Math Functions returm (same as 4 * 4 * 4)

c = pow(4, 3)

print(c)

# Built-in Math Functions returm (square root of a number)

import math

d = math.sqrt(64)

print(d)

# Built-in Math Functions returm (value rounds)

a = math.ceil(1.4)
e = math.floor(1.4)

print(a) # returns 2
print(e) # returns 1


# Built-in Math Functions returm (the value of PI)


f = math.pi

print(f)